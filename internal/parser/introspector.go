// extractVariableNameFromText extracts variable name from text
func (mi *ModuleIntrospector) extractVariableNameFromText(text string) string {
	// Look for variable patterns
	patterns := []*regexp.Regexp{
		regexp.MustCompile(`my\s+(\$\w+)`),
		regexp.MustCompile(`our\s+(\$\w+)`),
		regexp.MustCompile(`(\$\w+)\s*=`),
	}

	for _, pattern := range patterns {
		if matches := pattern.FindStringSubmatch(text); len(matches) > 1 {
			return matches[1]
		}
	}

	return ""
}

// contains checks if a slice contains a string
func contains(slice []string, item string) bool {
	for _, s := range slice {
		if s == item {
			return true
		}
	}
	return false
}

// ModuleIntrospectionResult represents the result of module introspection
type ModuleIntrospectionResult struct {
	// Path is the module file path
	Path string

	// SymbolTable contains all discovered symbols
	SymbolTable *SymbolTable

	// Packages maps package names to their information
	Packages map[string]*PackageInfo

	// DetectedFrameworks lists detected OOP frameworks
	DetectedFrameworks []string

	// DataStructures contains analyzed data structure information
	DataStructures map[string]*DataStructureInfo

	// Errors contains any errors encountered during introspection
	Errors []error

	// currentPackage tracks the current package context during analysis
	currentPackage string
}

// PackageInfo contains information about a package
type PackageInfo struct {
	// Name is the package name
	Name string

	// Methods contains all methods in the package
	Methods map[string]*MethodSignature

	// Attributes contains all attributes
	Attributes map[string]*AttributeInfo

	// ParentClasses lists parent classes
	ParentClasses []string

	// Roles lists consumed roles
	Roles []string
}

// DataStructureInfo represents analyzed data structure information
type DataStructureInfo struct {
	// Name is the variable name
	Name string

	// Type is the inferred type
	Type string

	// Structure describes the data structure
	Structure interface{}
}
