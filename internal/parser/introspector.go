// ModuleIntrospector provides deep introspection capabilities for Perl modules
type ModuleIntrospector struct {
	// Parser is the underlying parser for AST generation
	Parser Parser

	// SymbolTable tracks all symbols (functions, variables, packages) in the module
	SymbolTable *SymbolTable

	// MethodDetector identifies methods including dynamically generated ones
	MethodDetector *DynamicMethodDetector

	// OOPPatternDetector recognizes common OOP frameworks (Moose, Moo, etc.)
	OOPPatternDetector *OOPPatternDetector

	// DataStructureAnalyzer analyzes complex data structures
	DataStructureAnalyzer *DataStructureAnalyzer
}

// SymbolTable represents all symbols found in a module
type SymbolTable struct {
	// Packages maps package names to their symbols
	Packages map[string]*PackageSymbols

	// GlobalVariables tracks module-level variables
	GlobalVariables map[string]*VariableInfo

	// Imports tracks imported modules and their symbols
	Imports map[string]*ImportInfo
}

// PackageSymbols represents symbols within a package
type PackageSymbols struct {
	// Name is the package name
	Name string

	// Methods maps method names to their signatures
	Methods map[string]*MethodSignature

	// Attributes maps attribute names to their types
	Attributes map[string]*AttributeInfo

	// Constants maps constant names to their values
	Constants map[string]interface{}

	// ParentClasses lists parent classes for inheritance
	ParentClasses []string

	// Roles lists consumed roles (for Moose/Moo)
	Roles []string
}

// MethodSignature represents a method's signature with parameters and return type
type MethodSignature struct {
	// Name is the method name
	Name string

	// Parameters lists the method parameters
	Parameters []*ParameterInfo

	// ReturnType is the inferred or documented return type
	ReturnType string

	// IsAutoloaded indicates if this method is handled by AUTOLOAD
	IsAutoloaded bool

	// IsDynamic indicates if this method is dynamically generated
	IsDynamic bool

	// Source indicates how the method was detected (explicit, AUTOLOAD, generated, etc.)
	Source MethodSource

	// Documentation extracted from POD or comments
	Documentation string
}

// ParameterInfo represents information about a method parameter
type ParameterInfo struct {
	// Name is the parameter name
	Name string

	// Type is the inferred or documented type
	Type string

	// IsOptional indicates if the parameter is optional
	IsOptional bool

	// DefaultValue is the default value if any
	DefaultValue string

	// Documentation for the parameter
	Documentation string
}

// AttributeInfo represents information about a class attribute
type AttributeInfo struct {
	// Name is the attribute name
	Name string

	// Type is the attribute type
	Type string

	// IsRequired indicates if the attribute is required
	IsRequired bool

	// HasDefault indicates if the attribute has a default value
	HasDefault bool

	// DefaultValue is the default value if any
	DefaultValue string

	// Traits lists any attribute traits (for Moose/Moo)
	Traits []string

	// Documentation for the attribute
	Documentation string
}

// VariableInfo represents information about a variable
type VariableInfo struct {
	// Name is the variable name
	Name string

	// Type is the inferred type
	Type string

	// Scope is the variable scope (my, our, state)
	Scope string

	// InitialValue is the initial value if any
	InitialValue string
}

// ImportInfo represents information about an imported module
type ImportInfo struct {
	// ModuleName is the imported module name
	ModuleName string

	// ImportedSymbols lists explicitly imported symbols
	ImportedSymbols []string

	// ImportAll indicates if all symbols were imported
	ImportAll bool

	// Version is the required version if specified
	Version string
}

// MethodSource indicates how a method was detected
type MethodSource int

const (
	// MethodSourceExplicit for explicitly defined methods
	MethodSourceExplicit MethodSource = iota
	// MethodSourceAutoload for AUTOLOAD-handled methods
	MethodSourceAutoload
	// MethodSourceGenerated for methods generated by frameworks
	MethodSourceGenerated
	// MethodSourceInherited for inherited methods
	MethodSourceInherited
	// MethodSourceRole for methods from consumed roles
	MethodSourceRole
)

// DynamicMethodDetector detects dynamically generated methods
type DynamicMethodDetector struct {
	// Patterns for recognizing method generators
	GeneratorPatterns []*regexp.Regexp

	// KnownGenerators maps generator function names to their patterns
	KnownGenerators map[string]MethodGeneratorInfo
}

// MethodGeneratorInfo describes a method generator pattern
type MethodGeneratorInfo struct {
	// Pattern matches the generator usage
	Pattern *regexp.Regexp

	// ExtractMethodName extracts the generated method name
	ExtractMethodName func(match []string) string

	// InferSignature infers the method signature
	InferSignature func(match []string) *MethodSignature
}

// OOPPatternDetector recognizes OOP framework patterns
type OOPPatternDetector struct {
	// FrameworkPatterns maps framework names to their detection patterns
	FrameworkPatterns map[string]*FrameworkPattern

	// DetectedFrameworks lists detected OOP frameworks in the module
	DetectedFrameworks []string
}

// FrameworkPattern describes patterns for detecting an OOP framework
type FrameworkPattern struct {
	// Name is the framework name (Moose, Moo, Class::Tiny, etc.)
	Name string

	// UsePattern matches the use statement
	UsePattern *regexp.Regexp

	// AttributePattern matches attribute declarations
	AttributePattern *regexp.Regexp

	// MethodModifierPattern matches method modifiers (before, after, around)
	MethodModifierPattern *regexp.Regexp

	// ExtractAttribute extracts attribute info from a match
	ExtractAttribute func(node Node) *AttributeInfo

	// ExtractMethodModifier extracts method modifier info
	ExtractMethodModifier func(node Node) *MethodModifierInfo
}

// MethodModifierInfo represents a method modifier (before/after/around)
type MethodModifierInfo struct {
	// Type is the modifier type
	Type string

	// TargetMethod is the method being modified
	TargetMethod string

	// Implementation is the modifier implementation
	Implementation Node
}

// DataStructureAnalyzer analyzes complex data structures
type DataStructureAnalyzer struct {
	// TypeInferenceRules for inferring types from data structures
	TypeInferenceRules []TypeInferenceRule
}

// TypeInferenceRule represents a rule for inferring types from code patterns
type TypeInferenceRule struct {
	// Name is the rule name
	Name string

	// Pattern matches the code pattern
	Pattern *regexp.Regexp

	// InferType infers the type from the match
	InferType func(match []string) string
}

// NewModuleIntrospector creates a new module introspector
func NewModuleIntrospector() (*ModuleIntrospector, error) {
	parser, err := NewParser()
	if err != nil {
		return nil, fmt.Errorf("failed to create parser: %w", err)
	}

	introspector := &ModuleIntrospector{
		Parser:                parser,
		SymbolTable:           NewSymbolTable(),
		MethodDetector:        NewDynamicMethodDetector(),
		OOPPatternDetector:    NewOOPPatternDetector(),
		DataStructureAnalyzer: NewDataStructureAnalyzer(),
	}

	return introspector, nil
}

